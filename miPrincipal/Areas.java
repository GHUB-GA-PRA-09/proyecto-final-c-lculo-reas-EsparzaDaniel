package miPrincipal;

public class Areas {

    public int Cuadrado(int b) {
        return b * b;
    }
    
    public int Rectangulo(int b, int h) {
        return b * h ;
    }

    public int Triangulo(int b, int h) {
        return b * h / 2 ;
    }

    public int Romboide(int b, int h) {
        return b * h ;
    }
}
